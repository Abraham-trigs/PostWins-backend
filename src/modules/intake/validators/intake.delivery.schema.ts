import { z } from "zod";

export const IntakeDeliveryBodySchema = z.object({
  projectId: z.string().uuid(),
  deliveryId: z.string().min(1),
  occurredAt: z.union([z.string(), z.date()]),
  location: z.any(),
  items: z.array(z.any()).min(1),
  notes: z.string().optional(),
});

////////////////////////////////////////////////////////////////
// Design reasoning
////////////////////////////////////////////////////////////////
// Delivery schema validates execution evidence before
// writing to executionProgress.

